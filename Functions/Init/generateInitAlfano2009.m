function pp = generateInitAlfano2009(orbit)

%Case 11
mu  = 3.986004418e14;    % [m^3/s^2]
HBR = 4;                % [m]
x_s = [6878137; 0; 0; 0; 7612.6081732; 0];
x_d = [6878137; 76.126; 0; -0.08426; 7612.608; 0];

C_s = [8.578275 -1.389523 0 0 0 0;
       -1.389523 4.421725 0 0 0 0;
       0 0 4 0 0 0; 
       [zeros(3) 1e-8*eye(3)]];
C_d = [8.5783054 -1.389477 0 0 0 0;
       -1.389477 4.42169 0 0 0 0;
       0 0 4 0 0 0; 
       [zeros(3) 1e-8*eye(3)]];
% x_s = [153446.764628; 41874155.869566; 0; 3066.8747609105; -11.3736149565; 0];
% x_d = [153447.2642029; 41874156.369903; 5; 3066.8647607073; -11.3636148179; -0.00000013581];
% 
% C_s = [6494.179623 -376.138583 0 0.01598948 -0.494261677 0;
%        -376.138583 22.55946519 0 -0.00098831404 0.0285694606 0;
%        0 0 1.2050395223 0 0 -0.00006008763; 
%        0.01598948 -0.00098831404 0 4.43e-8 -1.212234e-6 0;
%        -0.494261677 0.0285694606 0 1.212234e-5 3.762297e-5 0;
%        0 0 -0.00006008763 0 0 3.39e-8];
C_d = C_s;

C_eci = C_d+C_s;
%% Inertial state of the two objects
coe = cartesian2kepler(x_d,mu);
T = 2*pi/coe.n;
%dcm from Hill to eci
dcm = lvlh2eci(x_s(1:3),x_s(4:6))*lvlh2hill()';
%% Relative initial conditions in Hill reference frame
C0 = zeros(6);
C0(1:3,1:3) = dcm'*C_eci(1:3,1:3)*dcm; C0(4:6,4:6) = dcm'*C_eci(4:6,4:6)*dcm;

pp = struct( ...
            'orbit', orbit, ...
            'mu',    mu, ...
            'HBR',   HBR, ...
            'x_s',   x_s, ...
            'x_d',   x_d, ...
            'C0',    C0, ...
            'T',     T ...
            );
end